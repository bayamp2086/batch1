package com.bayamp.archana.training.collections.comparator;

public class Iphone {

	private String model;
	private int memory;
	private String color;
	
	Iphone(String iModel,int iMemo,String iColor) {
		this.model = iModel;
		this.memory = iMemo;
		this.color = iColor;
	}
	 public String getModel() {
		return model;	 
	 }
	 
	 public int getMemory() {
			return memory;
	 }
	 public String getColor() {
		return color;
		 
	 }
	
	 
	 
}
